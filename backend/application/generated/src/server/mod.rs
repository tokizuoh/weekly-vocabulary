/// Setup API Server.
pub fn new<I, A>(api_impl: I) -> Router
where
    I: AsRef<A> + Clone + Send + Sync + 'static,
    A: Api + 'static,
{
    // build our application with a route
    Router::new()
        .route("/delete/:id",
            delete(delete_id_delete::<I, A>)
        )
        .route("/get/all",
            get(get_all_get::<I, A>)
        )
        .route("/get/recent",
            get(get_recent_get::<I, A>)
        )
        .route("/register",
            post(register_post::<I, A>)
        )
        .route("/update",
            put(update_put::<I, A>)
        )
        .with_state(api_impl)
}


